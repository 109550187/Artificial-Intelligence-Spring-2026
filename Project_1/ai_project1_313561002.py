def sample_dataframe_by_fraction(df, fraction, target_col="viral", random_state=42):
    sampled_parts = []
    for cls, grp in df.groupby(target_col):
        sampled_parts.append(grp.sample(frac=fraction, random_state=random_state))
    return pd.concat(sampled_parts).sample(frac=1.0, random_state=random_state).reset_index(drop=True)

