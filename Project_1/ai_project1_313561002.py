def search_video_ids(youtube, keyword, order="relevance", max_pages=2, per_page=50, published_after=None):
    all_ids = []
    next_page_token = None

    for page_idx in range(max_pages):
        request_kwargs = dict(
            part="snippet",
            q=keyword,
            type="video",
            order=order,
            maxResults=per_page,
            pageToken=next_page_token,
        )
        if published_after:
            request_kwargs["publishedAfter"] = published_after

        request = youtube.search().list(**request_kwargs)
        response = request.execute()

        items = response.get("items", [])
        ids = [item["id"]["videoId"] for item in items if "videoId" in item.get("id", {})]
        all_ids.extend(ids)

        print(f"keyword={keyword!r}, order={order}, page={page_idx+1}, fetched_ids={len(ids)}, total_ids={len(all_ids)}")

        next_page_token = response.get("nextPageToken")
        if not next_page_token:
            break

    return all_ids


def fetch_video_details(youtube, video_ids, batch_size=50):
    all_items = []

    for i in range(0, len(video_ids), batch_size):
        batch_ids = video_ids[i:i+batch_size]
        request = youtube.videos().list(
            part="snippet,contentDetails,statistics",
            id=",".join(batch_ids),
            maxResults=batch_size,
        )
        response = request.execute()
        items = response.get("items", [])
        all_items.extend(items)
        print(f"Fetched details for batch {i//batch_size + 1}, items={len(items)}, total_items={len(all_items)}")

    return all_items

